package com.example.theshoppingmileapp.dominio;

public class ListPlace {

    private String head;
    private String desc;

    public ListPlace(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
